      <article class="blog-post">
        <h2 class="blog-post-title">Review: OnePlus 10 Pro</h2>
        <p class="blog-post-meta">December 14, 2020 by <a href="#">Julian Chokkattu</a></p>

        <p>IF YOU MADE me choose from the current crop of flagship Android smartphones, the new OnePlus 10 Pro wouldn't be my first or second choice. It's not my last either. At $899, it sits in an awkward spot of being pretty good for the money, but not quite as great as some of its peers. </p>
        <ul>
          <li>Bright, 120-Hz screen. OK battery life with fast wired and wireless charging. Great performance. Solid camera system for photos and video. Customizable, simple software experience. Will get three OS upgrades and four years of security updates. Nice haptics.</li>
          <li>Camera system isn't as good as similarly priced competitors, especially in low light. No millimeter-wave 5G, no 5G support on AT&T. No IP rating on unlocked model.</li>
          <li>Rating: 7/10</li>
        </ul>
        <p>I've only run into two bugs on my OnePlus 10 Pro review unit. Sometimes, the camera app freezes and it requires a full phone restart to get things back to normal. Also, I wasn't able to make contactless payments twice … which also required a restart for it to work again. OnePlus says it hasn't seen these problems and that the team is looking into it. 

          Outside of that, I've had a pretty enjoyable time with the 10 Pro. I'm nitpicky about the camera, but you have to be when that's the area of biggest focus and improvement on smartphones nowadays. The 10 Pro also has a pretty reliable fingerprint sensor; I love the haptic vibrations that rumble out of it; and I've grown to like the physical mute switch on the side (like on iPhones).</p>
